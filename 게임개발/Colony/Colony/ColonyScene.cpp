{
	switch (nMessageID)
	{
	case WM_KEYDOWN:
		switch (wParam)
		{
		case '1':
		case '2':
			if (m_ppHierarchicalGameObjects[0]) m_ppHierarchicalGameObjects[0]->SetTrackAnimationSet(0, int(wParam) - '1');
			break;
		}
		break;
	default:
		break;
	}
	return(false);

}


